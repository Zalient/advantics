package com.university.grp20.model;

public class MetricsDTO {
  private double impressions;
  private double clicks;
  private double uniques;
  private double bounces;
  private double conversions;
  private double totalCost;
  private double ctr;
  private double cpa;
  private double cpc;
  private double cpm;
  private double bounceRate;

  public double getImpressions() {
    return impressions;
  }

  public void setImpressions(double impressions) {
    this.impressions = impressions;
  }

  public double getClicks() {
    return clicks;
  }

  public void setClicks(double clicks) {
    this.clicks = clicks;
  }

  public double getUniques() {
    return uniques;
  }

  public void setUniques(double uniques) {
    this.uniques = uniques;
  }

  public double getBounces() {
    return bounces;
  }

  public void setBounces(double bounces) {
    this.bounces = bounces;
  }

  public double getConversions() {
    return conversions;
  }

  public void setConversions(double conversions) {
    this.conversions = conversions;
  }

  public double getTotalCost() {
    return totalCost;
  }

  public void setTotalCost(double totalCost) {
    this.totalCost = totalCost;
  }

  public double getCtr() {
    return ctr;
  }

  public void setCtr(double ctr) {
    this.ctr = ctr;
  }

  public double getCpa() {
    return cpa;
  }

  public void setCpa(double cpa) {
    this.cpa = cpa;
  }

  public double getCpc() {
    return cpc;
  }

  public void setCpc(double cpc) {
    this.cpc = cpc;
  }

  public double getCpm() {
    return cpm;
  }

  public void setCpm(double cpm) {
    this.cpm = cpm;
  }

  public double getBounceRate() {
    return bounceRate;
  }

  public void setBounceRate(double bounceRate) {
    this.bounceRate = bounceRate;
  }
}
